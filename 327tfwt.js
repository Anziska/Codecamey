function boolToWord(bool) {
	// function with a Variable and a argument
	// A boolean answers  true/false
	return bool ? "Yes" : "No";
	//We want it to return Yes or No and not True or False
	// return with the argument
	// ? and : short version of the if...else statement

	//if bool is true return 'Yes' else if bool is false return 'No'
}
